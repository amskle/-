// 请编写程序计算数字三角形从顶部到底部的路径上的元素最大和。

// 3

// 1 6 

// 2 8 4

// 4 1 9 7

// 从三角形的顶部到底部有很多条不同的路径。对于每条路径，把路径上面的数加起来可以得到一个和，你的任务就是找到最大的和。
// 从顶部到底部的路径要求：只能从一个数走到下一层的同列或右边那个数。如上述数字三角形中从3出发可以选择下一层的1或6，如果选择1，同样也只能选择下一层的2或8。

// 输入格式:
// 输入的第一行包含一个整数 n（1<n<=10)，表示三角形的行数。

// 下面的 n 行给出数字三角形。数字三角形上的数都是 0 至 100 之间的整数。

// 输出格式:
// 输出一个整数，表示答案。

// 输入样例:
// 4
// 3
// 1 6 
// 2 8 4
// 4 1 9 7
// 输出样例:
// 26



#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;
int main() {
    int n;
    if (!(cin >> n)) return 0;

    // 使用二维数组存储三角形
    vector<vector<int>> triangle(n, vector<int>(n));
    for (int i = 0; i < n; i++) {
        for (int j = 0; j <= i; j++) {
            cin >> triangle[i][j];
        }
    }

    // 从倒数第二层开始向上递推
    // dp[i][j] 直接复用 triangle 数组以节省空间
    for (int i = n - 2; i >= 0; i--) {
        for (int j = 0; j <= i; j++) {
            // 当前位置的最大和 = 当前值 + 下一层相邻两个位置中的较大者
            triangle[i][j] += max(triangle[i + 1][j], triangle[i + 1][j + 1]);
        }
    }

    // 最终顶点存储的就是路径最大和
    cout << triangle[0][0] << endl;

    return 0;
}