// 输入样例:
// 1
// 7
// 输出样例:
// 6
#include <iostream>
#include <vector>
#include <algorithm>
#include <string>
using namespace std;
const int MAXN = 1000001;
const int MOD = 1000000000;

// 全局数组防止栈溢出
int f[MAXN];

void precompute() {
    f[0] = 1;
    f[1] = 1;
    for (int i = 2; i < MAXN; i++) {
        if (i % 2 != 0) {
            // 奇数情况
            f[i] = f[i - 1];
        } else {
            // 偶数情况：f(2k) = f(2k-2) + f(k)
            f[i] = (f[i - 2] + f[i / 2]) % MOD;
        }
    }
}
int main() {
    // 快速 I/O
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    // 预处理所有可能的 n，避免多组数据重复计算
    precompute();

    int t;
    if (!(cin >> t)) return 0;
    
    while (t--) {
        int n;
        cin >> n;
        cout << f[n];
        if (t > 0) cout << " "; // 控制输出格式：所有输出占一行，行末无空格
    }
    cout << endl;

    return 0;
}