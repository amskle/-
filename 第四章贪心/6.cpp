// 给出一个穿墙魔术师的能量以及一个表演舞台，要求在舞台上拆除最少数量的墙，使得穿墙魔术师可以沿任意观众选择的列穿过所有的墙。

// 输入格式:
// 输入的第一行给出一个整数 t (1≤t≤10) ，表示测试用例的个数，然后给出每个测试用例的数据。
// 每个测试用例的第一行给出两个整数 n（ 1≤n≤100 ），表示墙的面数；和 k（ 0≤k≤100 ），表示穿墙魔术师可以通过的墙的最大面数。在这一行后，给出 n 行，每行包含两个（ x, y ）对，表示一面墙的两个端点坐标。坐标是小于等于 100 的非负整数。左上角的方格的坐标为（ 0, 0 ）。下面给出的第二个测试样例相应于上图。

// 输出格式:
// 对每个测试用例，输出一行，给出一个整数，表示最少拆除墙的面数，使得穿墙魔术师能从上方任何一列开始穿越。

// 输入样例:
// 2
// 3 1
// 2 0 4 0
// 0 1 1 1
// 1 2 2 2
// 7 3
// 0 0 3 0
// 6 1 8 1
// 2 3 6 3
// 4 4 6 4
// 0 5 1 5
// 5 6 7 6
// 1 7 3 7
// 输出样例:
// 1
// 1
#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        int n, k;
        cin >> n >> k;

        // 每面墙：[左端列, 右端列]
        vector<pair<int,int>> walls(n);
        for (int i = 0; i < n; i++) {
            int x1, y1, x2, y2;
            cin >> x1 >> y1 >> x2 >> y2;
            walls[i] = { min(x1, x2), max(x1, x2) };
        }

        int ans = 0;
        vector<bool> removed(n, false);

        // 扫描每一列（坐标 ≤ 100）
        for (int col = 0; col <= 100; col++) {
            // 收集当前列的活跃墙
            vector<int> active;
            for (int i = 0; i < n; i++) {
                if (!removed[i] &&
                    walls[i].first <= col &&
                    walls[i].second >= col) {
                    active.push_back(i);
                }
            }

            // 按右端点降序排列（优先拆右端点最远的）
            sort(active.begin(), active.end(), [&](int a, int b) {
                return walls[a].second > walls[b].second;
            });

            // 超出 k 面，贪心拆除
            while ((int)active.size() > k) {
                removed[active[0]] = true;
                active.erase(active.begin());
                ans++;
            }
        }

        cout << ans << "\n";
    }
    return 0;
}