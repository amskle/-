// 从前，在没有刷卡和扫码支付的年代。人们需要自备零钱乘坐公交车。

// 已知公交车票是5角钱。有多位乘客待上车，有人准备的是5角钱，有人只有1元钱。持1元钱的乘客希望找回5角钱。那么，请你来替大家算算，有多少种不同的上车顺序，使得持有1元钱的乘客能拿回5角钱。

// 例如，持有5角的乘客2位，持有1元的乘客1位，则合理的上车顺序有2种，分别是5角、5角、1元，和5角、1元、5角。

// 输入格式:
// 在一行中给出2个整数A和B，代表持有5角的乘客数量和持有1元的乘客数量，其中，0 <= A, B <= 30。

// 保证输出结果在long long范围内。

// 输出格式:
// 输出有多少种合理的上车顺序。

// 输入样例:
// 2 1
// 输出样例:
// 2
#include <iostream>
using namespace std;

int main() {
    int A, B;
    cin >> A >> B;

    // 如果 1 元乘客数量大于 5 角乘客数量，不可能合法
    if (B > A) {
        cout << 0 << endl;
        return 0;
    }

    long long dp[31][31] = {0};

    // 初始化：只有 5 角乘客的情况
    for (int i = 0; i <= A; i++) {
        dp[i][0] = 1;
    }

    // 动态规划
    for (int i = 1; i <= A; i++) {
        for (int j = 1; j <= B; j++) {
            if (j <= i) {
                dp[i][j] = dp[i-1][j] + dp[i][j-1];
            } else {
                dp[i][j] = 0;
            }
        }
    }

    cout << dp[A][B] << endl;

    return 0;
}