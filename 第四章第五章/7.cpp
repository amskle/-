#include <bits/stdc++.h>
using namespace std;

const int MAXN = 1e5 + 5;
const int INF = 1e9;

vector<int> graph[MAXN];
int distEven[MAXN];  // 偶数步到达的最短距离
int distOdd[MAXN];   // 奇数步到达的最短距离

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    
    int n, m, q;
    cin >> n >> m >> q;
    
    for (int i = 0; i < m; i++) {
        int u, v;
        cin >> u >> v;
        graph[u].push_back(v);
        graph[v].push_back(u);
    }
    
    // 初始化距离数组
    for (int i = 1; i <= n; i++) {
        distEven[i] = INF;
        distOdd[i] = INF;
    }
    
    // BFS 计算奇偶最短路径
    queue<pair<int, int>> bfs; // (节点, 步数奇偶性 0偶数 1奇数)
    distEven[1] = 0;
    bfs.push({1, 0});
    
    while (!bfs.empty()) {
        auto [node, parity] = bfs.front();
        bfs.pop();
        
        int currentDist = (parity == 0) ? distEven[node] : distOdd[node];
        
        for (int next : graph[node]) {
            int nextParity = parity ^ 1;
            int nextDist = currentDist + 1;
            
            if (nextParity == 0) {
                if (nextDist < distEven[next]) {
                    distEven[next] = nextDist;
                    bfs.push({next, 0});
                }
            } else {
                if (nextDist < distOdd[next]) {
                    distOdd[next] = nextDist;
                    bfs.push({next, 1});
                }
            }
        }
    }
    
    // 处理查询
    while (q--) {
        int a, L;
        cin >> a >> L;
        
        bool canReach = false;
        if (L % 2 == 0) {
            // 需要偶数步到达
            if (distEven[a] <= L) {
                canReach = true;
            }
        } else {
            // 需要奇数步到达
            if (distOdd[a] <= L) {
                canReach = true;
            }
        }
        
        cout << (canReach ? "Yes" : "No") << '\n';
    }
    
    return 0;
}